export const getIconForFile = (filename: string): Component => {
  const extension = filename.split('.').pop()?.toLowerCase();

  switch (extension) {
    case 'ts':
    case 'tsx':
      return TypeScriptIcon;
    case 'js':
    case 'jsx':
      return JavaScriptIcon;
    case 'json':
      return JsonIcon;
    case 'css':
      return CssIcon;
    case 'html':
      return HtmlIcon;
    case 'svg':
      return ImageIcon;
    case 'md':
      return MarkdownIcon;
    case 'musicxml':
      return MusicXMLIcon;
    case 'mid':
    case 'midi':
      return MidiIcon;
    case 'track':
      return TrackIcon;
    case 'widget':
      return WidgetIcon;
    default:
      return DefaultFileIcon;
  }
};